    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
            String code = txtCode.getText().trim();
            int discount = Integer.parseInt(txtDiscount.getText());
            if(code.trim().equals("") || discount <= 0 || discount > 99){
                JOptionPane.showMessageDialog(null, "Invalid code/discount!");
                return;
            }

            Pattern pattern = Pattern.compile("^[a-zA-Z0-9]+$");
            Matcher matcher = pattern.matcher(code);
            if(!matcher.matches()){
                JOptionPane.showMessageDialog(null, "Invalid code!");
                return;
            }
            
            for(Promo p: business.getPromoDirectory().getPromoList()){
                if(p.getCode().equals(code)){
                    JOptionPane.showMessageDialog(null, "Code already exists!");
                    return;
                }
            }
            business.getPromoDirectory().addPromo(code, discount);
            JOptionPane.showMessageDialog(null, "Promo successfully added!");
            populateTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid code/discount!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            String code = txtCode.getText().trim();
            int discount = Integer.parseInt(txtDiscount.getText());
            if(code.trim().equals("") || discount <= 0 || discount > 99){
                JOptionPane.showMessageDialog(null, "Invalid code/discount!");
                return;
            }
            
            if(this.promo == null){
                JOptionPane.showMessageDialog(null, "Invalid code/discount!");
                return;
            }

            Pattern pattern = Pattern.compile("^[a-zA-Z0-9]+$");
            Matcher matcher = pattern.matcher(code);
            if(!matcher.matches()){
                JOptionPane.showMessageDialog(null, "Invalid code!");
                return;
            }
            this.promo.setCode(code);
            this.promo.setDiscountPercent(discount);
            
            JOptionPane.showMessageDialog(null, "Promo successfully updated!");
            populateTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid code/discount!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


