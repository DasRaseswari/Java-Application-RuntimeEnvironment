    private void populateTable(){
        tblPromo.removeAll();
        DefaultTableModel dtm = (DefaultTableModel) tblPromo.getModel();
        dtm.setRowCount(0);
        
        for(Promo promo: business.getPromoDirectory().getPromoList()){
            Object[] row = new Object[2];
            row[0] = promo;
            row[1] = promo.getDiscountPercent();
            dtm.addRow(row);
        }
    }

