    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblPromo.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Please select a promo first!");
            return;
        }
        
        int dialog = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Promo??");
        if(dialog == 1){
            Promo promo = (Promo) tblPromo.getValueAt(row, 0);
            business.getPromoDirectory().getPromoList().remove(promo);
            JOptionPane.showMessageDialog(null, "Promo successfully deleted!");
            populateTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

